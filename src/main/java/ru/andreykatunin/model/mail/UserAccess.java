package ru.andreykatunin.model.mail;

public class UserAccess {
    private String email;
    private String uuid;

    public UserAccess() {
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }
}
